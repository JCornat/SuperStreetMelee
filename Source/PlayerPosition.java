
public class PlayerPosition {

	int x,y,w,h;
	
	public PlayerPosition(int k, int l) {
		x = 0;
		y = 0;
		w = k;
		h = l;
	}
	
	public int getX() {
		return x;
	}
	
	public void setX(int i) {
		x = i;
	}
	
	public int getY() {
		return y;
	}
	
	public void setY(int i) {
		y = i;
	}
	
	public int getW() {
		return w;
	}
	
	public void setW(int i) {
		w = i;
	}
	
	public int getH() {
		return h;
	}
	
	public void setH(int i) {
		h = i;
	}

}
